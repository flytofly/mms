package cn.mmdata.mms.data.tag.client;

public class QueryRulesRuleInfo {
	private int ruleid;
	private String webname;
	
	public QueryRulesRuleInfo(int ruleid, String webname) {
		super();
		this.ruleid = ruleid;
		this.webname = webname;
	}

	public QueryRulesRuleInfo() {
		super();
	}

	public int getRuleid() {
		return ruleid;
	}

	public void setRuleid(int ruleid) {
		this.ruleid = ruleid;
	}


	public String getWebname() {
		return webname;
	}


	public void setWebname(String webname) {
		this.webname = webname;
	}


	@Override
	public String toString() {
		return "RuleInfo [ruleid=" + ruleid + ", webname=" + webname + "]";
	}
}